
var Add = function (x, y )
            {
                return x+y;
            }

            
var Sub = function (x, y )
            {
                return x-y;
            }

var Mult = function (x, y )
            {
                return x*y;
            }


module.exports = {
                    Addition : Add,
                    Subtract : Sub,
                    Mult
                 };

